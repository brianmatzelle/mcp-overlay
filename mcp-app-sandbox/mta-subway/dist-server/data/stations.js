/**
 * Search stations by name (fuzzy partial match).
 * Optionally filter by line.
 */
export function searchStations(query, line) {
    const q = query.toLowerCase().trim();
    const upperLine = line?.toUpperCase();
    return Object.values(STATIONS).filter((s) => {
        const nameMatch = s.name.toLowerCase().includes(q);
        const lineMatch = upperLine ? s.lines.includes(upperLine) : true;
        return nameMatch && lineMatch;
    });
}
/**
 * Resolve a station query to a single StationInfo.
 * Tries: direct stop_id, exact name match, partial match.
 */
export function resolveStation(line, query) {
    const upperLine = line.toUpperCase();
    const normalized = query.trim();
    // 1. Direct stop_id match
    if (STATIONS[normalized.toUpperCase()]) {
        return STATIONS[normalized.toUpperCase()];
    }
    if (STATIONS[normalized]) {
        return STATIONS[normalized];
    }
    // 2. Exact name match on the requested line
    const exact = Object.values(STATIONS).find((s) => s.name.toLowerCase() === normalized.toLowerCase() &&
        s.lines.includes(upperLine));
    if (exact)
        return exact;
    // 3. Partial name match on the requested line
    const partials = Object.values(STATIONS).filter((s) => s.name.toLowerCase().includes(normalized.toLowerCase()) &&
        s.lines.includes(upperLine));
    if (partials.length >= 1)
        return partials[0];
    // 4. Broader search without line filter
    const any = Object.values(STATIONS).find((s) => s.name.toLowerCase().includes(normalized.toLowerCase()));
    if (any)
        return any;
    throw new Error(`Station not found: "${query}" on ${upperLine} line`);
}
